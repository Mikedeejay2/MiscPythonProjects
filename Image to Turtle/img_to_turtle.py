x = -256
y = 256
step = 16
pen_width = 16
draw_mode = "line" # line, circle, square

def get_color(index):
    return colordb[index]

def draw_shape():
    if draw_mode == "line":
        turtle.forward(step)
    elif draw_mode == "circle":
        turtle.circle(1)
        turtle.penup()
        turtle.forward(step)
        turtle.pendown()
    elif draw_mode == "square":
        for i in range(4):
            turtle.forward(step - pen_width)
            turtle.right(90)
        turtle.penup()
        turtle.forward(step)
        turtle.pendown()

def draw_block(single_block):
    global x, y
    width = len(single_block[0])
    start_y = y
    for pixel_strip in single_block:
        turtle.pendown()
        for pixel in pixel_strip:
            turtle.color(get_color(pixel))
            draw_shape()
        turtle.penup()
        y -= step
        turtle.goto(x, y)
    x += width * step
    turtle.goto(x, start_y)
    y = start_y

turtle = trtl.Turtle()
turtle.speed(0)
trtl.colormode(255)
turtle.penup()
turtle.goto(x, y)
turtle.width(pen_width)
trtl.tracer(0, 0)

for block_strips in blocks:
    start_x = x
    start_y = y
    height = len(block_strips[0])
    for single_block in block_strips:
        draw_block(single_block)
    turtle.penup()
    x = start_x
    y = start_y - height * step
    turtle.goto(x, y)

